package org.mypathus.tsgforce.model;

public class TemplateXLSIndividualLevelLoginStatistics {
	
	private int totalLogin;
	private String loginDate;
	private String loginTime;
	private String logoutTime;
	private String loginDuration;
	
	public int getTotalLogin() {
		return totalLogin;
	}
	public void setTotalLogin(int totalLogin) {
		this.totalLogin = totalLogin;
	}
	public String getLoginDate() {
		return loginDate;
	}
	public void setLoginDate(String loginDate) {
		this.loginDate = loginDate;
	}
	public String getLoginTime() {
		return loginTime;
	}
	public void setLoginTime(String loginTime) {
		this.loginTime = loginTime;
	}
	public String getLogoutTime() {
		return logoutTime;
	}
	public void setLogoutTime(String logoutTime) {
		this.logoutTime = logoutTime;
	}
	public String getLoginDuration() {
		return loginDuration;
	}
	public void setLoginDuration(String loginDuration) {
		this.loginDuration = loginDuration;
	}
	
	
	
}

package org.mypathus.tsgforce.model;

public class TemplateXLSConsolidatedUserInformation {

	private String userName;
	private String lastName;
	private String programSite;
	private String loginAmounts;
	private String loginsLength;
	private boolean isExpenseTrackerCompleted;
	private boolean isMyBudgetCompleted;
	private boolean isSavingGeneratorCompleted;
	
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getProgramSite() {
		return programSite;
	}
	public void setProgramSite(String programSite) {
		this.programSite = programSite;
	}
	public String getLoginAmounts() {
		return loginAmounts;
	}
	public void setLoginAmounts(String loginAmounts) {
		this.loginAmounts = loginAmounts;
	}
	public String getLoginsLength() {
		return loginsLength;
	}
	public void setLoginsLength(String loginsLength) {
		this.loginsLength = loginsLength;
	}
	public boolean isExpenseTrackerCompleted() {
		return isExpenseTrackerCompleted;
	}
	public void setExpenseTrackerCompleted(boolean isExpenseTrackerCompleted) {
		this.isExpenseTrackerCompleted = isExpenseTrackerCompleted;
	}
	public boolean isMyBudgetCompleted() {
		return isMyBudgetCompleted;
	}
	public void setMyBudgetCompleted(boolean isMyBudgetCompleted) {
		this.isMyBudgetCompleted = isMyBudgetCompleted;
	}
	public boolean isSavingGeneratorCompleted() {
		return isSavingGeneratorCompleted;
	}
	public void setSavingGeneratorCompleted(boolean isSavingGeneratorCompleted) {
		this.isSavingGeneratorCompleted = isSavingGeneratorCompleted;
	}
	
	


}
